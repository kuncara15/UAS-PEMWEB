                        <p class="lead mt-5">
                            Sebelum pandemik, pasar sepeda listrik terus berkembang. Sekarang ini, secara global, penggunaan sepeda juga terus meningkat. Jadi tidak salah bagi Harley untuk masuk ke pasar dengan masa depan yang lebih diminati. Sepeda listrik Harley Davidson Serial 1 adalah produk terbaru dari Harley Davidson yang berkolaborasi dengan Serial 1. Membuat ebike dengan bentuk klasik dan tangguh, yang diperlengkapi dengan teknologi dan komponen modern. Konsep sepeda listrik Harley-Davidson sudah diperlihatkan sejak awal 2019. Sepeda listrik Harley-Davidson Serial 1 adalah kolaborasi Harley-Davidson dengan brand start up Serial 1. Serial 1 (www.serial1.com) sendiri adalah perusahaan start up yang fokus untuk pengembangan sepeda listrik, yang dipimpin oleh mantan karyawan Harley-Davidson. Nama Serial 1 diambil dari “Serial Number One”, nostalgia untuk bentuk dan nama motor pertama/tertua Harley-Davidson, yang dibuat pada tahun 1903.</p>

                            <p>Sepeda ini memakai sistem penggerak mid hub motor (posisi motor di tengah/chainring), tidak memakai sistem rantai, tetapi mekai sistem drive belt (Gates carbon belt). Seperti yang kita tahu, dengan mid hub motor, sepeda listrik akan terasa lebih natural, berbeda dengan motor penggerak di roda depan atau belakang yang sensasi tarikannya berbeda dengan sepeda biasa. Ditambah single chainring drive belt, untuk efisiensi tenaga yang lebih tinggi, suara yang senyap, sert perawatan yang lebih mudah. Sepeda listrik Harley-Davidson Serial 1 menggunakan pedal-assist, dimana motor listrik akan aktif ketika pedal kita kayuh, membantu mendorong sepeda, membuat kayuhan yang ringan. Tidak ada tuas gas/throttle, dan kecepatan dibatasi pada sekitar 20 atau 28 mph (sekitar 45 km/jam), mengikuti peraturan batasan kecepatan sepeda listrik e-pedelec pada umumnya. Yang membedakan sepeda listrik HD dengan sepeda listrik lainnya adalah tampilannya. Tampilan sepeda listrik ini sangat menarik. Geometri frame mungkin terlihat simpel, nuansanya sangat klasik dan retro, dengan tidak menghilangkan aura Harley Davidson yang bertenaga. Harley Davidson tidak hanya membuat motor gede, mereka juga sebenarnya sudah mempunyai produk sepeda motor listrik yaitu LiveWire, termasuk balance bike (sepeda anak) yang bertenaga listrik. Dengan pengalaman ini, sepeda listrik HD Serial 1 ini harusnya akan mempunyai performa yang lebih terjamin.

