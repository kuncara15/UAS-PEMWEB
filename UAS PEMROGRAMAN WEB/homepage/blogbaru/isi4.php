                        <p class="lead mt-5">
                            <h4>1. Cek Rantai Sepeda</h4>
                            <ul type="square">
                                Ban pada sepeda listrik perlu diperhatikan karena tekanan ban bisa berdampak pada konsumsi baterai dimana semakin lunak baterai sepeda, semakin berat juga gerak sepeda. Jika tekanan ban kurang optimal maka sepeda juga jadi lebih sulit dikendalikan, khususnya ketika dibawa ke medan yang sulit. Ban yang terisi cukup udara juga berperforma lebih baik saat melakukan pengereman. Pastikan saat memompa ban sepeda listrik, kamu juga harus memperhatikan PSI yang terdapat pada ban ya. Untuk kamu yang lagi cari pompa lantai yang sudah dilengkapi dengan gauge atau alat ukur, kami rekomendasikan pompa Polygon Pompa Lantai STX.
                            </ul>

                            <h4>2. Tekanan Ban Harus Dijaga</h4>
                            <ul type="square">
                                Pada sepeda komponen rantai adalah komponen yang harus diperhatikan. Karena rantai menghubungkan mesin motor dengan roda belakang, khususnya pada sepeda listrik yang menggunakan mesin motor mid-drive. Agar awet pesepeda bisa memilih pelumas kering atau basah untuk melumasi rantai sepeda, apa bedanya? Pelumas basah umumnya bertahan lebih lama, bahkan saat bersepeda dalam kondisi buruk sementara pelumas kering tidak memiliki ketahanan seperti pelumas basah, tetapi cenderung meninggalkan hasil lebih bersih.
                                Pengecekan rantai juga harus rutin dilakukan untuk melihat apakah rantai melar atau tidak, hal ini dilakukan karena rantai sepeda listrik dilewati tegangan dua kali lebih besar dibanding sepeda non-elektrik. Pengecekan ini bisa dilakukan dengan menggunakan checker rantai sepeda. Checker rantai sepeda adalah alat yang digunakan untuk mengetahui kelayakan kondisi rantai.
                            </ul>

                            <h4>3. Cas Baterai Sepeda Listrik Berkala</h4>
                            <ul type="square">
                                jika SobatSS ada keinginan untuk mencuci sepeda listrik, maka tips dari kami adalah, sebisa mungkin jangan langsung menyemprot sepeda listrik dengan air secara langsung ke bagian seperti foot pad dan kontroler, karena bagian foot pad di dalamnya terdapat sebuah aki yang bisa korslet jika tersemprot air. Kamu bisa menggunakan lap basah untuk mengelap bagian  ya. Namun tentu pada bagian ban, kamu tetap bisa membasuhnya dengan air kok! Tetapi dengan intensitas air yang rendah ya!
                            </ul>

                            <h4>4. Cek Kualitas Rem</h4>
                            <ul type="square">
                                Pastikan rem tetap pakem ya, jika saat pengereman pada brake lever dirasa cukup jauh atau tidak pakem maka yang bisa dilakukan adalah putar setelan baut yang ada di roda depan searah jarum jam.
                            </ul></p>

