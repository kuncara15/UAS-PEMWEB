                        <p class="lead mt-5">
                            <h4>Sepeda Balap:</h4>
                            <ul type="square">
                                Sepeda balap atau bahasa bekennya adalah Road Bike merupakan sepeda yang diperuntukan di medan jalan raya ber-aspal. Road Bike sangat cocok untuk kamu yang lebih suka dengan kecepatan dan kelincahan saat berkendara gowes. Secara garis besar, Road Bike sendiri dibagi menjadi 3 kategori yakni aero, endurance dan lightweight.
                                <li>Basah dan Kedinginan.
                                </li>
                                <li>Cobaan Semakin Berat.</li>
                                <li>Jarak Pandang Terbatas.</li>
                                <li>Kejutan Spesial dari Halilintar.</li>
                                <li>AKomponen Sepeda Cepat Rusak.</li>
                            </ul>

                            <h4>Sepeda Hybrid:</h4>
                            <ul type="square">
                                Sepeda Hybrid sering sekali disebut sebagai sepeda dua alam. Istilah hybrid sendiri digunakan untuk menyebut kombinasi antara dua jenis konsep yang berbeda, dimana sepeda ini merupakan gabungan antara Road Bike dan juga MTB. Memiliki feel berkendara seperti MTB namun memiliki kelincahan seperti Road Bike. Namun jika dilihat sekilas, walaupun penampakan Sepeda Hybrid terlihat seperti MTB, namun memiliki perbedaan yaitu : 
                                <li>Fork</li>
                                MTB dibekali dengan travel fork yang tinggi untuk menerjang jalanan offroad sedang cenderung tinggi. Fork Sepeda Hybrid dibuat lebih pendek karena peruntukan Sepeda Hybrid adalah berkendara di medan aspal mulus, sedikit berbatu, hingga offroad yang cenderung ringan. Pada Sepeda Hybrid biasanya dibekali dengan travel yang tidak sampai 100mm.
                                <li>Stang</li>
                                Pada hybrid memiliki stang yang sama seperti MTB, yaitu handlebar yang berbentuk lurus. Stang model lurus memberikan feel berkendara yang lebih rileks namun tetap stabil saat sepeda melewati jalanan yang sedikit rusak. 
                                </li>Ban</li>
                                Berikut alasan kenapa jenis sepeda ini memiliki tagline melesat lebih cepat di medan jalan rata. Itu Karena Sepeda Hybrid memiliki ban yang lebih ramping daripada MTB. Jika biasanya ban MTB memiliki ban model knobby yang tinggi, pada Sepeda Hybrid hadir dengan ban semi slick hingga knobby yang rendah supaya.
                            </ul>

                            <h4>Sepeda Gravel:</h4>
                            <ul type="square">
                                Jika Sepeda Hybrid memiliki geometri yang “mirip” dengan MTB, kali ini ada Sepeda Gravel hadir dengan geometri yang “mirip” dengan Road Bike. Kata gravel sendiri jika diterjemahkan ke dalam bahasa indonesia memiliki arti “kerikil” sehingga sepeda ini cocok digunakan di medan jalan raya berkerikil yang sering sekali kita temui di Indonesia. Mau punya sepeda yang bisa dipakai di 2 alam namun mempunyai profil seperti Road Bike? mungkin jenis Sepeda Gravel cocok untuk kamu nih!
                                <li>Fork</li>
                                Berbeda dengan Sepeda Hybrid yang memiliki suspensi di bagian forknya, pada fork Sepeda Gravel memiliki fork rigid seperti Road Bike. 
                                <li>Stang</li>
                                Umumnya stang Sepeda Gravel hadir dengan stang dropbar model flare out. flare out artinya stang yang posisi dropnya lebih melebar keluar. Stang flare out membuat pegangan yang sedikit lebih lebar. Stang model itu berguna untuk membantu meningkatkan keseimbangan, tumpuan, dan kontrol pada jalan tidak rata dan banyak menanjak. Seperti sepeda gunung dengan pegangan stang yang datar, flared out membuat kita bisa lebih kuat bertumpu pada stang, karena pegangan lebih susah untuk lepas karena guncangan jalan. 
                                </li>Ban</li>
                                Nah ini juga yang menjadi pembeda antara Road Bike dengan gravel. jika pada Road Bike memiliki ban dengan ukuran yang kecil dan tidak berulir, pada Sepeda Gravel memiliki lebar ban yang cukup besar dan memiliki knob.
                            </ul>

